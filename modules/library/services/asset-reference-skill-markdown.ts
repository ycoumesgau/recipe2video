import type { AssetLibraryEntry } from "@/modules/references/repositories/asset-library.repository";

import {
  ASSET_LIBRARY_CATEGORIES,
  ASSET_LIBRARY_CATEGORY_DISPLAY,
  ASSET_LIBRARY_WORKSPACE_FOLDER_BY_CATEGORY,
  type AssetLibraryCategory,
  isAssetLibraryCategory,
} from "../library.constants";

/**
 * Hand-curated preamble for the asset-reference-system skill. Kept identical
 * across regenerations so reviewers focus on the inventory diff, not prose
 * drift. If the rules ever change, update this string and ship a deliberate
 * commit.
 */
const SKILL_FRONTMATTER = `---
name: asset-reference-system
description: Select and name real character, kitchen, pose, expression, and utensil references from \`assets/\`.
---`;

const SKILL_INTRO = `# Asset Reference System

Use whenever a scene, segment, or reference plan mentions kitchen, character, pose, expression, or utensils.

## Rules

- Use only real files from \`assets/\`.
- Do not invent canonical reference names.
- A canonical asset name is the file name without extension.
- Global references cover kitchen, character, poses, expressions, and common utensils.
- Recipe-specific references cover food states Seedance may misread.
- Do not re-describe the Licorn character from scratch. Refer to character references and specify only pose, expression, action, and visible hands.
- For every reference in a Seedance prompt, state exactly what role it plays.
- A reference appears AT MOST once in \`reference-plan.json\`. If multiple segments reuse it, list them all in \`usedInSegmentIds\`.
- Prefer a canonical library asset over generating a recipe-specific one. Only declare a recipe-specific reference when nothing in this list fits.
- Every Seedance segment should include the kitchen continuity pair: \`@KitchenLayoutContextWide\` (structural context) plus one shot-specific kitchen view (\`@KitchenIslandDefault\` OR overhead/induction/oven variants).
- \`@KitchenLayoutContextWide\` is a context lock, not a camera framing requirement.
- Do not force \`@KitchenIslandDefault\` when another kitchen angle is the active shot view; use it when needed for the actual shot or explicit terrazzo lock.
- Keep kitchen invariants stable across segments: same light terrazzo countertop, same induction geometry, same cabinet layout.
- Match utensil choice to action physics (for deep-fry retrieval, prefer \`@SpiderSkimmer\`; avoid \`@SiliconeSpatula\` for that. For lifting portions from baking dishes, prefer \`@TurningSpatula\`; for fragile pastry transfers, \`@OffsetSpatula\`).
- Avoid cloth-in-hand hot transfer prompts due hand/cloth fusion risk.
- Quantify side/garnish amounts in prompts and keep prop continuity when relevant.

## Conditioning recipe-specific references

Recipe-specific references (e.g. \`FinishedDumplingLasagnaCutaway\`, \`ParisBrestBakedCrown\`) are produced by GPT-Image 2 inside Recipe2Video. Without explicit anchors the model invents the kitchen and pan from scratch and breaks continuity with the Seedance segments that consume the reference.

For every recipe-specific entry in \`reference-plan.json\`, declare a \`conditioningReferences\` array listing the library globals GPT-Image 2 should ground the image on. Names use the same form as Seedance \`@Tag\` references.

Minimum coverage for a recipe-state reference:
- one kitchen view (\`KitchenIslandDefault\` or the relevant overhead / induction / oven variant),
- the cookware that holds the dish (\`baking_dish\`, \`SaucepanLarge\`, …),
- the dominant utensil when applicable.

**Do NOT include character anchors** (\`Character-sheet\`, character poses like \`Luma-front-pose\`, expressions like \`Facial-expressions\`) — they are filtered out at generation time because the mascot adds noise to dish frames. The kitchen anchor alone carries the Licorn visual identity for recipe-state images.

Example:

\`\`\`json
{
  "type": "recipe_state",
  "canonicalName": "FinishedDumplingLasagnaCutaway",
  "role": "finished compact dumpling-lasagna top and cutaway geometry",
  "priority": 1,
  "usedInSegmentIds": ["segment-01", "segment-07", "segment-08"],
  "prompt": "Generate one vertical-reference still of a compact 8 in / 20 cm square steamed dumpling lasagna…",
  "conditioningReferences": [
    "KitchenIslandDefault",
    "baking_dish",
    "Spatula"
  ]
}
\`\`\`

Do NOT include the recipe-specific reference itself in \`conditioningReferences\` — only library canonical names or aliases belong here.`;

const SKILL_FOOTER = `If an object does not exist as an asset, describe it in the prompt but do not list it as a reference.`;

const AUTOGEN_NOTICE = (timestampUtc: string) =>
  `<!-- AUTO-GENERATED by the Recipe2Video /library admin page on ${timestampUtc}. Do not edit by hand: changes will be overwritten the next time an operator updates the library. -->`;

/**
 * Render a single inventory line for the skill. We expose the friendliest
 * known alias (first one declared, if any) as the @-handle the agent will
 * mention in prompts, but always link back to the real file path so the agent
 * can audit what the asset actually contains.
 */
function renderEntry(entry: AssetLibraryEntry): string {
  const category = entry.category as AssetLibraryCategory;
  const folder = ASSET_LIBRARY_WORKSPACE_FOLDER_BY_CATEGORY[category];
  const displayHandle = entry.aliases[0]?.trim() || entry.canonicalName;
  const filePath = `assets/${folder}/${entry.canonicalName}.png`;
  // Description is optional — when present we keep it as a trailing clause so
  // the line stays readable even when no description is set.
  const description = (entry.description ?? "").trim();
  const suffix = description ? ` — ${description}` : "";
  return `- \`@${displayHandle}\` from \`${filePath}\`${suffix}`;
}

/**
 * Pure markdown generator. Given a list of library entries (typically the
 * `active` ones), produce the canonical SKILL.md content. The output is
 * deterministic so the regenerator can short-circuit the GitHub PUT when the
 * library hasn't materially changed.
 *
 * Deprecated entries are intentionally NOT rendered: the agent should never
 * pick a deprecated reference for a new generation. We surface them only on
 * the /library page (admin context).
 */
export function renderAssetReferenceSkillMarkdown(input: {
  entries: AssetLibraryEntry[];
  generatedAtUtc?: string;
}): string {
  const generatedAt = input.generatedAtUtc ?? new Date().toISOString();

  // Sort entries category-first (using the canonical order from
  // ASSET_LIBRARY_CATEGORIES), then alphabetically by display handle so the
  // output is stable regardless of insertion order.
  const grouped = new Map<AssetLibraryCategory, AssetLibraryEntry[]>();
  for (const category of ASSET_LIBRARY_CATEGORIES) {
    grouped.set(category, []);
  }
  for (const entry of input.entries) {
    if (entry.status !== "active") continue;
    if (!isAssetLibraryCategory(entry.category)) continue;
    grouped.get(entry.category)!.push(entry);
  }
  for (const list of grouped.values()) {
    list.sort((a, b) => {
      const aHandle = (a.aliases[0] || a.canonicalName).toLowerCase();
      const bHandle = (b.aliases[0] || b.canonicalName).toLowerCase();
      return aHandle.localeCompare(bHandle);
    });
  }

  const sections: string[] = [];
  for (const category of ASSET_LIBRARY_CATEGORIES) {
    const list = grouped.get(category) ?? [];
    if (list.length === 0) continue;
    const heading = `### ${ASSET_LIBRARY_CATEGORY_DISPLAY[category]}`;
    const lines = list.map(renderEntry);
    sections.push([heading, "", ...lines].join("\n"));
  }

  const inventory =
    sections.length > 0
      ? sections.join("\n\n")
      : "_No active library entries — seed the library or add one from /library._";

  return [
    SKILL_FRONTMATTER,
    "",
    SKILL_INTRO,
    "",
    "## Canonical Library",
    "",
    AUTOGEN_NOTICE(generatedAt),
    "",
    inventory,
    "",
    SKILL_FOOTER,
    "",
  ].join("\n");
}
